                    <h5><strong>ASSISTANCE GRANT</strong></h5>
                    <p>
                        <strong>WHAT IS A ASSISTANCE GRANT?</strong><br />
                        It is a one time payment for students, who find themselves temporarily in a difficult life situation.
                        <br /><br />
                        <strong>WHO CAN APPLY?</strong><br />
                        Full-time and part-time students from all of the fields of studies, including international students and 1st semester students.
                        <br /><br />
                        <strong>HOW OFTEN CAN BE GRANTED?</strong><br />
                        It may be awarded once a semester ( twice in the academic year).
                        <br /><br />
                        <strong>WHAT IS THE BASIS FOR GRANTING?</strong><br />
                        The basis for application is a difficult life situation temporarily, in particular as a result of:<br />
                        •	an accident, serious illness of the student and the closest family member,<br />
                        •	death of the closest family member of the student (child, spouse, father, mother, siblings, legal or actual guardian) – provided that he / she lived in a common household with the student,<br />
                        •	theft or destruction of basic necessities through no fault of the student,<br />
                        •	destruction of the student’s flat, house or farm as a result of a natural disaster or other random event,<br />
                        •	non-culpable loss of job by the student, his parents, spouse, legal or actual guardians, provided that he / she lived in the common household with the student.
                        <br /><br />
                        <strong>IMPORTANT INFORMATION:</strong><br />
                        1.	An application for assistance should be submitted not later than within two months from the date of the event entitling the person to apply for the benefit.<br />
                        2.	The circumstances given by the student as the basis for applying for the assistance should be documented.<br />
                        3.	It cannot be awarded twice for the same circumstances.
                        <br /><br />
                        <strong>Examples of supporting documents:</strong><br />
                        •	documents of the Registry Office,<br />
                        •	medical certificates,<br />
                        •	certificates from the police, fire brigade, insurance institution, other certificates of competent institutions or offices confirming the circumstances described in the application,<br />
                        •	certificates from the Labor Office,<br />
                        •	employment certificate.
                        <br /><br />
                        <strong>HOW TO APPLY?</strong><br />
                        Submit the application together with supporting document:<br />
                        1.	in Polish Deanery ( room no.27 )<br />
                        2.	sent via email, ONLY from UITM students email account on address stypendia@wsiz.edu.pl<br />
                        3.	via post office as a registered letter to the address: Wyższa Szkoła Informatyki i Zarządzania w Rzeszowie 35-225 Rzeszów ul. Sucharskiego 2 with the note : Scholarship Department
